package com.Kizunad.customNPCs.ai.sensors;

/**
 * 传感器事件类型 - 用于分级感知事件的重要性
 * <p>
 * 此枚举定义了三个级别的感知事件,用于控制 NPC 的中断响应机制:
 * - INFO: 普通信息,不触发中断
 * - IMPORTANT: 重要信息,可能触发中断
 * - CRITICAL: 紧急情况,立即触发中断并忽略滞后阈值
 */
public enum SensorEventType {
    /**
     * 信息级 (INFO) - 普通信息更新
     * <p>
     * 示例场景:
     * - 发现掉落物品
     * - 普通环境变化
     * - 常规的视野更新
     * <p>
     * 行为:
     * - 仅更新记忆系统
     * - 不触发目标重新评估中断
     * - 等待常规评估周期 (20 ticks)
     */
    INFO,

    /**
     * 重要级 (IMPORTANT) - 重要信息
     * <p>
     * 示例场景:
     * - 发现玩家
     * - 发现高价值目标
     * - 检测到友好实体
     * <p>
     * 行为:
     * - 更新记忆系统
     * - 触发目标重新评估中断
     * - 遵守滞后 (Hysteresis) 阈值,避免频繁切换
     */
    IMPORTANT,

    /**
     * 紧急级 (CRITICAL) - 紧急情况
     * <p>
     * 示例场景:
     * - 受到伤害
     * - 生命值危险 (低于 10%)
     * - 检测到强大敌人
     * - 检测到致命危险 (岩浆、悬崖)
     * <p>
     * 行为:
     * - 更新记忆系统
     * - 立即触发目标重新评估中断
     * - 忽略滞后阈值,允许立即切换目标
     * - 受中断冷却机制限制 (防止每 tick 都触发)
     */
    CRITICAL,
}
